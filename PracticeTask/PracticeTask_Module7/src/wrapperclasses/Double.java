package wrapperclasses;

public class Double {
    public static void main(String[] args) {
        //минимальное и максимально значение double
        System.out.println(java.lang.Double.MIN_VALUE);
        System.out.println(java.lang.Double.MAX_VALUE);

        System.out.println("---------------------------");

        //бесконечность double
        System.out.println(java.lang.Double.NEGATIVE_INFINITY);
        System.out.println(java.lang.Double.POSITIVE_INFINITY);
    }
}
