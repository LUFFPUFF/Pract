package typeprimitive.fractionalnumbers;

public class Float {
    public static void main(String[] args) {
        //float принимает вещественные числа до 8-ми цифр
        float a = 6.8990f;
        System.out.println(a);

        float PI = (float) Math.PI;
        System.out.println(PI);
    }
}
